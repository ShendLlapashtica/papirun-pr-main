export function getIngredientName(key: string, lang: 'sq' | 'en'): string {
  const entry = ingredientNames[key.toLowerCase()];
  if (entry) return entry[lang];
  // Fallback: capitalize the key
  return key.charAt(0).toUpperCase() + key.slice(1);
}
